-- E4.3 — DOF/BLGF 2024 LGU income reclassification (docs/EXPLORE_ENHANCEMENT_PLAN.md §E4.3).
--
-- RA 11964 (Automatic Income Classification of LGUs Act) replaced the old six-class
-- income ladder (1st..6th, DOF DO 23-08 vintage) with FIVE classes (1st..5th) and
-- recomputed every province / city / municipality from FY2021-2023 average regular
-- income. DOF Department Order No. 074-2024 (Annex A) is the authoritative schedule;
-- it took effect 01 January 2025. dim_geo.income_class previously carried the
-- StepZero-reported (pre-reclassification) class; this migration refreshes it to the
-- DO 074-2024 value, preserving the prior value in income_class_prior for provenance.
--
-- The DOF Annex carries no PSGC codes and labels regions with the pre-NIR vintage, so
-- the geo_code join is name-based, province-scoped and NIR-aware. The full mapping,
-- reconciliation and eyeball-verified fixups are produced by
-- ingestion/build_income_reclass.py and reviewed into
-- ingestion/data/income_reclass_2024.csv (which also carries the source LGU names /
-- regions for audit; only geo_code + classes are loaded here). Write-up:
-- docs/INCOME_RECLASS.md. Applied live via the Supabase MCP.
--
-- Coverage: 1637/1651 city/municipalities and 81/82 provinces classified. The 14
-- unclassified city/muni rows are expected: 6 the source leaves unclassified ("New"
-- newly-created LGUs + a dash for Ubay, Bohol) and 8 BARMM Special Geographic Areas
-- (not classified as LGUs by DOF); Eastern Samar province is omitted from the source.
-- These retain their prior class (or stay null) and are logged in docs/INCOME_RECLASS.md.

-- 1) Provenance column: the class value being superseded (StepZero / DO 23-08 era).
alter table dim_geo add column if not exists income_class_prior smallint;
comment on column dim_geo.income_class is
  'LGU income class. Refreshed by E4.3 to DOF DO 074-2024 (RA 11964, 5-class, eff. 2025-01-01) where classified; a few LGUs retain the prior value (see dim_lgu_income_reclass / income_class_prior).';
comment on column dim_geo.income_class_prior is
  'Pre-reclassification income class (StepZero-reported, ~DO 23-08 vintage) preserved by E4.3 before dim_geo.income_class was refreshed to DOF DO 074-2024.';

