-- 3) Auditable per-LGU reclassification table (geo_code -> class link; source LGU names
--    kept in ingestion/data/income_reclass_2024.csv, not duplicated here).
create table if not exists dim_lgu_income_reclass (
  reclass_id   bigint generated always as identity primary key,
  geo_code     text not null references dim_geo (geo_code),
  geo_level    geo_level_enum not null,
  dof_kind     text not null check (dof_kind in ('province','city','municipality')),
  old_class_dof smallint,                    -- "old" column in the Annex (DOF DO 23-08, 1..6)
  new_class    smallint check (new_class between 1 and 5),  -- DO 074-2024 class (null = source unclassified)
  converted    boolean not null default false,               -- city classified "as Municipality" note
  match_method text not null check (match_method in ('fuzzy','province','override')),
  match_score  smallint,
  dataset_id   bigint references dim_dataset (dataset_id),
  unique (geo_code, dataset_id)
);
create index if not exists dim_lgu_income_reclass_new_class_idx on dim_lgu_income_reclass (new_class);
comment on table dim_lgu_income_reclass is
  'DOF DO 074-2024 (RA 11964) income reclassification mapped onto dim_geo. E4.3; source LGU names in ingestion/data/income_reclass_2024.csv; see docs/INCOME_RECLASS.md.';

alter table dim_lgu_income_reclass enable row level security;
drop policy if exists "dim_lgu_income_reclass public read" on dim_lgu_income_reclass;
create policy "dim_lgu_income_reclass public read" on dim_lgu_income_reclass
  for select to anon, authenticated using (true);

